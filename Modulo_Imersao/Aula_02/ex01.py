num1 = float(input("Ditie o número 01: "))
num2 = float(input("Ditie o número 02: "))
num3 = float(input("Ditie o número 03: "))

soma = num1 + num2 + num3
produto = num1 * num2 * num3
media = soma / 3

print(f"A soma dos três número é {soma}")
print(f"O produto dos três número é {produto}")
print(f"A media dos três número é {media}")